//(Додатковачастина для завдання)
let form1 = document.forms.form1
form1.onsubmit = function (e) {
    e.preventDefault();
    let line = this.line.value;
    let cell = this.cell.value;
    let info = this.info.value;
    let items = {line, cell, info};
    let users = [];
    users.push(items);
    console.log(users);

    let target = document.getElementsByClassName('target')[0];
    target.style.background = 'red'
    target.style.height = '500px'
    for (let user of users)
    {
        for (let i = 0; i < `${user.line}`; i++)
        {
            let div = document.createElement('div');
            target.appendChild(div);

            div.classList.add ('divclass');
            let divclass = document.getElementsByClassName('divclass')[0];
            divclass.style.display = 'flex';
            divclass.style.justifyContent = 'space-around'; //не розумію, чого спрацьовує лише для першого блоку

            for ( let j = 0; j<`${user.cell}`;j++)
            {
                let div2 = document.createElement('div');
                div.appendChild(div2);
                let h6 = document.createElement('h6');
                div2.appendChild(h6);
                h6.innerText = `${user.info}`;

            }

        }

    }
}





